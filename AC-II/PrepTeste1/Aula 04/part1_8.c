#include <detpic32.h>

int display7Scodes[] = {0x3F, 0x06, 0x5B, 0x4F, 0x66, 0x6D, 0x7D, 0x07, 0x7F, 0x6F, 0x77, 0x7C, 0x39, 0x5E, 0x79, 0x71};

int main(void) {
    LATB = LATB & 0x80FF;
    TRISB |= 0x000F;
    TRISB &= 0x80FF;
    TRISD = TRISD & 0xFFDF;

    while(1){
        int ds = PORTB & 0x000F;
        LATB = (display7Scodes[ds] << 8) | (LATB & 0x00FF);
    }
    
    return 0;
}
